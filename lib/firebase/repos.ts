/**
 * Firestore repositories (Data Access Layer).
 *
 * Thin, typed wrappers over the modular Firebase client SDK and the typed
 * collection / document helpers in `lib/firebase/converters.ts`. Each repository
 * exposes the smallest surface the Admin_Panel and Project_Viewer need, reading
 * and writing the domain models from `lib/types.ts` without scattering casts.
 *
 * Design notes:
 * - Document ids carry the domain `id` (the converters strip the stored `id`
 *   field on write and merge the document id back on read), so callers always
 *   receive fully-formed domain objects.
 * - Each `Project` document id equals its `Project_ID` (Req 2.2). New ids are
 *   minted by the pure `generateUniqueProjectId` generator, checked for
 *   collisions through `projectRepo.exists` (Req 2.3).
 * - Lead pipeline rules (default `New` status, timeline appends) are owned by
 *   the pure helpers in `lib/leads.ts`; the repository only persists their
 *   results (Req 20.4, 38.2, 38.3).
 * - The repositories use the client SDK and obtain the Firestore handle from
 *   `getClientFirestore()` lazily, so a single instance is reused.
 *
 * Requirements: 2.2, 2.3, 20.2, 33.2, 33.3, 37.1, 37.2, 37.3, 38.2, 38.3
 */

import {
  addDoc,
  deleteDoc,
  doc,
  getDoc,
  getDocs,
  query,
  setDoc,
  updateDoc,
  where,
  writeBatch,
  type DocumentReference,
} from "firebase/firestore";

import { getClientFirestore } from "@/lib/firebase/client";
import {
  leadsCollection,
  plotsCollection,
  projectDoc,
  projectsCollection,
  statusGroupsCollection,
  zonesCollection,
} from "@/lib/firebase/converters";
import { appendNote, appendStatusChange, createLead } from "@/lib/leads";
import { generateUniqueProjectId } from "@/lib/projectId";
import type {
  Lead,
  LeadStatus,
  Plot,
  Project,
  StatusGroup,
  Zone,
} from "@/lib/types";

// ---------------------------------------------------------------------------
// Input shapes
// ---------------------------------------------------------------------------

/**
 * Fields supplied when creating a {@link Project}. The `id` is generated by the
 * repository (Req 2.1) and the `createdAt` / `updatedAt` timestamps are managed
 * on write, so all three are omitted from the caller's input.
 */
export type ProjectInput = Omit<Project, "id" | "createdAt" | "updatedAt">;

/** Fields supplied when creating a {@link Zone}; the id is auto-generated. */
export type ZoneInput = Omit<Zone, "id">;

/** Fields supplied when creating a {@link StatusGroup}; id is auto-generated. */
export type StatusGroupInput = Omit<StatusGroup, "id">;

/**
 * Fields supplied when capturing a new {@link Lead} from an enquiry (Req 20.2).
 * The repository assigns the Firestore document id, defaults the status to
 * `New` and the timeline to empty via {@link createLead} (Req 20.4), and
 * defaults `createdAt` to the current time when omitted.
 */
export type NewLeadData = Omit<Lead, "id" | "status" | "timeline" | "createdAt"> & {
  createdAt?: number;
};

// ---------------------------------------------------------------------------
// Project repository (Req 2.2, 2.3, 33.2)
// ---------------------------------------------------------------------------

export const projectRepo = {
  /**
   * Lists every Project in the projects collection (Req 33.1, 33.2). Used by
   * the Admin_Panel Projects manager to render the inventory of developments.
   */
  async list(): Promise<Project[]> {
    const snapshot = await getDocs(projectsCollection(getClientFirestore()));
    return snapshot.docs.map((d) => d.data());
  },

  /**
   * Loads a Project by id, or `null` when no document exists.
   *
   * @param id The Project_ID (equals the Firestore document id, Req 2.2).
   */
  async get(id: string): Promise<Project | null> {
    const snapshot = await getDoc(projectDoc(getClientFirestore(), id));
    return snapshot.exists() ? snapshot.data() : null;
  },

  /**
   * Reports whether a Project document with the given id already exists. Used
   * as the collision predicate for {@link generateUniqueProjectId} (Req 2.3).
   *
   * @param id Candidate Project_ID.
   * @returns `true` when the id is already taken.
   */
  async exists(id: string): Promise<boolean> {
    const snapshot = await getDoc(projectDoc(getClientFirestore(), id));
    return snapshot.exists();
  },

  /**
   * Creates a Project, minting a unique 5–6 character Project_ID (Req 2.1) that
   * does not collide with any existing project (Req 2.3) and persisting the
   * document under that id (Req 2.2, 33.2). The `createdAt` / `updatedAt`
   * timestamps are stamped to the current time.
   *
   * @param data The project fields (everything except id and timestamps).
   * @returns The generated Project_ID.
   */
  async create(data: ProjectInput): Promise<string> {
    const db = getClientFirestore();
    const id = await generateUniqueProjectId((candidate) => this.exists(candidate));
    const now = Date.now();
    const project: Project = {
      ...data,
      id,
      createdAt: now,
      updatedAt: now,
    };
    await setDoc(projectDoc(db, id), project);
    return id;
  },

  /**
   * Applies a partial update to a Project and refreshes its `updatedAt`
   * timestamp (Req 33.2). The `id` field is never changed.
   *
   * @param id The Project_ID to update.
   * @param patch The fields to change.
   */
  async update(id: string, patch: Partial<Project>): Promise<void> {
    const { id: _ignored, ...rest } = patch;
    void _ignored;
    await updateDoc(projectDoc(getClientFirestore(), id), {
      ...rest,
      updatedAt: Date.now(),
    });
  },

  /**
   * Deletes a Project document (Req 33.2). Destructive; gated to superadmin in
   * the access policy and Security Rules (Req 32.3).
   *
   * @param id The Project_ID to delete.
   */
  async remove(id: string): Promise<void> {
    await deleteDoc(projectDoc(getClientFirestore(), id));
  },
};

