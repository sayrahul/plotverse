// Lead repository (Req 20.2, 38.2, 38.3)
// ---------------------------------------------------------------------------

export const leadRepo = {
  /**
   * Captures a new Lead from an enquiry, referencing the Project and Plot
   * (Req 20.2). The status is defaulted to `New` and the timeline to empty via
   * {@link createLead} (Req 20.4), and the Firestore document id is generated
   * on write.
   *
   * @param data The enquiry fields (project, plot, contact details, message).
   * @returns The generated lead document id.
   */
  async create(data: NewLeadData): Promise<string> {
    const lead = createLead({
      ...data,
      id: "",
      createdAt: data.createdAt ?? Date.now(),
    });
    const ref = await addDoc(leadsCollection(getClientFirestore()), lead);
    return ref.id;
  },

  /**
   * Lists leads, optionally restricted to a single project (Req 38.1). When
   * `projectId` is supplied the query filters on the stored `projectId` field.
   *
   * @param projectId Optional Project_ID to scope the leads to.
   */
  async list(projectId?: string): Promise<Lead[]> {
    const collectionRef = leadsCollection(getClientFirestore());
    const snapshot = await getDocs(
      projectId === undefined
        ? collectionRef
        : query(collectionRef, where("projectId", "==", projectId)),
    );
    return snapshot.docs.map((d) => d.data());
  },

  /**
   * Applies a partial update to a lead (Req 38.1). The `id` field is never
   * changed. To record a status transition in the timeline, prefer
   * {@link leadRepo.changeStatus}; to add a note, use {@link leadRepo.addNote}.
   *
   * @param id The lead document id.
   * @param patch The fields to change.
   */
  async update(id: string, patch: Partial<Lead>): Promise<void> {
    const { id: _ignored, ...rest } = patch;
    void _ignored;
    await updateDoc(leadDoc(id), rest);
  },

  /**
   * Changes a lead's pipeline status and records the transition as a single
   * `status_change` timeline entry via {@link appendStatusChange} (Req 38.2,
   * 38.3). The lead is read, transitioned in memory, then its `status` and
   * `timeline` are persisted.
   *
   * @param id The lead document id.
   * @param toStatus The status to move the lead to.
   * @param by Optional identifier of the actor performing the change.
   * @returns The updated Lead.
   * @throws {Error} When no lead with the given id exists.
   */
  async changeStatus(id: string, toStatus: LeadStatus, by?: string): Promise<Lead> {
    const ref = leadDoc(id);
    const snapshot = await getDoc(ref);
    if (!snapshot.exists()) {
      throw new Error(`Lead not found: ${id}`);
    }
    const updated = appendStatusChange(snapshot.data(), toStatus, by);
    await updateDoc(ref, { status: updated.status, timeline: updated.timeline });
    return updated;
  },

  /**
   * Adds a note to a lead and records it as a single `note` timeline entry via
   * {@link appendNote} (Req 38.3). The lead is read, annotated in memory, then
   * its `timeline` is persisted.
   *
   * @param id The lead document id.
   * @param note The note text to record.
   * @param by Optional identifier of the actor adding the note.
   * @returns The updated Lead.
   * @throws {Error} When no lead with the given id exists.
   */
  async addNote(id: string, note: string, by?: string): Promise<Lead> {
    const ref = leadDoc(id);
    const snapshot = await getDoc(ref);
    if (!snapshot.exists()) {
      throw new Error(`Lead not found: ${id}`);
    }
    const updated = appendNote(snapshot.data(), note, by);
    await updateDoc(ref, { timeline: updated.timeline });
    return updated;
  },
};

/** Builds a typed reference to a lead document in the top-level collection. */
function leadDoc(id: string): DocumentReference<Lead> {
  return doc(leadsCollection(getClientFirestore()), id);
}
