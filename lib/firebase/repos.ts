// Plot repository (Req 33.3, 35.1, 37.3)
// ---------------------------------------------------------------------------

export const plotRepo = {
  /**
   * Lists all plots belonging to a project.
   *
   * @param projectId The owning Project_ID.
   */
  async list(projectId: string): Promise<Plot[]> {
    const snapshot = await getDocs(plotsCollection(getClientFirestore(), projectId));
    return snapshot.docs.map((d) => d.data());
  },

  /**
   * Loads a single plot, or `null` when it does not exist.
   *
   * @param projectId The owning Project_ID.
   * @param plotId The plot document id.
   */
  async get(projectId: string, plotId: string): Promise<Plot | null> {
    const ref = doc(plotsCollection(getClientFirestore(), projectId), plotId);
    const snapshot = await getDoc(ref);
    return snapshot.exists() ? snapshot.data() : null;
  },

  /**
   * Creates or updates many plot documents in a single atomic batch, keyed by
   * each plot's `id` (Req 33.3). Used when saving converted GeoJSON to upsert
   * the project's plot inventory (Req 35.1).
   *
   * @param projectId The owning Project_ID.
   * @param plots The plots to write; existing documents are overwritten.
   */
  async upsertMany(projectId: string, plots: Plot[]): Promise<void> {
    const db = getClientFirestore();
    const collectionRef = plotsCollection(db, projectId);
    const batch = writeBatch(db);
    for (const plot of plots) {
      batch.set(doc(collectionRef, plot.id), plot);
    }
    await batch.commit();
  },

  /**
   * Applies a partial update to a single plot (Req 33.3), e.g. changing its
   * status or assigning it to a zone (Req 37.3).
   *
   * @param projectId The owning Project_ID.
   * @param plotId The plot document id.
   * @param patch The fields to change.
   */
  async update(projectId: string, plotId: string, patch: Partial<Plot>): Promise<void> {
    const { id: _ignored, ...rest } = patch;
    void _ignored;
    const ref = doc(plotsCollection(getClientFirestore(), projectId), plotId);
    await updateDoc(ref, rest);
  },
};

